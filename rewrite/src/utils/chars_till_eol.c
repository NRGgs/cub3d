#include "../../include/cub3d.h"

size_t	chars_till_eol(char *str)
{
	size_t	i;

	i = 0;
	while (str[i] != '\0' && str[i] != '\n')
		i++;
	if (i == 0)
		return (0);
	return (i + 1);
}
