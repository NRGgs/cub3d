#include "../../include/cub3d.h"

size_t	count_char(char *str, char c)
{
	int		i;
	size_t	n;

	i = 0;
	n = 0;
	while (str[i] != '\0')
	{
		if (str[i] == c)
			n++;
		i++;
	}
	return (n);
}
